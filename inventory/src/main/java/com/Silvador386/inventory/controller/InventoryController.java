package com.Silvador386.inventory.controller;


import com.Silvador386.inventory.dto.InventoryResponse;
import com.Silvador386.inventory.service.InventoryService;
import lombok.RequiredArgsConstructor;
import org.springframework.http.HttpStatus;
import org.springframework.web.bind.annotation.*;

import java.util.List;

@RestController
@RequestMapping("/api/inventory")
@RequiredArgsConstructor
public class InventoryController {

    private final InventoryService inventoryService;

    // "http://localhost:8082/api/inventory?skuCode=Samsung-Galaxy&skuCode=Samsung-Universe"
    @GetMapping
    @ResponseStatus(HttpStatus.OK)
    public List<InventoryResponse> isInStock(@RequestParam List<String> skuCode){
        return inventoryService.isInStock(skuCode);
    }
}
